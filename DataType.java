public class DataType{
	public static void main(String args[]){
		int a = 10;
		float b = 10.35f;
		double c = 154.54;
		char d = 'R';
		boolean e = true;

		System.out.println("the values of that declared of the int is " + a);
		System.out.println("the values of that declared of the float is " + b);
		System.out.println("the values of that declared of the double is " + c);
		System.out.println("the values of that declared of the char is " + d);
		System.out.println("the values of that declared of the boolean is " + e);
	}
}